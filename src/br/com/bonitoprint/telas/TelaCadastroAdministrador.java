    private void BotaoCadAdmSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoCadAdmSalvarActionPerformed
        // TODO add your handling code here:
       
        String nome = CampoNome.getText();
        String email = CampoEmail.getText();
        String senha = String.valueOf(CampoSenha.getPassword());
        String senha2 = String.valueOf(CampoConfirmaSenha.getPassword());
               
        Administrador adm = new Administrador(nome,email,senha,senha2);
        
        AdministradorController admcontroller = new AdministradorController();
        
        try{
            if(senha.equals(senha2)){
                admcontroller.adicionar(adm);
                JOptionPane.showMessageDialog(rootPane,"Administrador Cadastrado com Sucesso", "Cadastro de Usuario", JOptionPane.INFORMATION_MESSAGE);      
                CampoNome.setText("");
                CampoEmail.setText("");
                CampoSenha.setText("");
                CampoConfirmaSenha.setText("");
            }else{
            JOptionPane.showMessageDialog(rootPane,"Senhas n√£o Conferem", "Login", JOptionPane.ERROR_MESSAGE); 
            }
            }catch(UsuarioExistenteException e){
              JOptionPane.showMessageDialog(rootPane, e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
            } catch (ErroInternoException ex) {
             JOptionPane.showMessageDialog(rootPane, ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
           }catch(CampoObrigatorioException cx){
             JOptionPane.showMessageDialog(rootPane, cx.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);  
           }
        
    }//GEN-LAST:event_BotaoCadAdmSalvarActionPerformed

