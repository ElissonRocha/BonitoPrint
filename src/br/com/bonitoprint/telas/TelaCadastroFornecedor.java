    private void BotaoFornecedorClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoFornecedorClienteActionPerformed
        // TODO add your handling code here:
        String nome = CampoFornecedorNome.getText();
        String email = CampoFornecedorEmail.getText();
        String cpf = CampoFornecedorCpf.getText();
        String rua = CampoFornecedorRua.getText();
        String complemento = CampoFornecedorComplemento.getText();
        String bairro = CampoFornecedorBairro.getText();
        String cidade = CampoFornecedorCidade.getText();
        String estado = CampoFornecedorEstado.getText();
        String tel1= CampoTel1.getText();
        String tel2 = CampoTel2.getText();
        String num = CampoFornecedorNumero.getText();
        if(num.length()<=0){
           JOptionPane.showMessageDialog(rootPane,"Campo Obrigatorio", "ERRO", JOptionPane.ERROR_MESSAGE);
        }else{
          int numero = Integer.parseInt(CampoFornecedorNumero.getText()); 
         Fornecedor f = new Fornecedor(tel1,tel2,nome,email,cpf,rua,numero,complemento,bairro,cidade,estado);
         FornecedorController fc = new FornecedorController();
        try{
            fc.adicionar(f);
            JOptionPane.showMessageDialog(rootPane,"Fornecedor Cadastrado com Sucesso", "Cadastro de Usuario", JOptionPane.INFORMATION_MESSAGE);
            CampoFornecedorNome.setText("");
            CampoFornecedorEmail.setText("");
            CampoFornecedorCpf.setText("");
            CampoFornecedorRua.setText("");
            CampoFornecedorComplemento.setText("");
            CampoFornecedorBairro.setText("");
            CampoFornecedorCidade.setText("");
            CampoFornecedorEstado.setText("");
            CampoFornecedorNumero.setText("");
            CampoTel1.setText("");
            CampoTel2.setText("");
        }catch(ErroInternoException erro ){
             JOptionPane.showMessageDialog(rootPane,erro.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }catch(UsuarioExistenteException ex){
             JOptionPane.showMessageDialog(rootPane,ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }catch(CampoObrigatorioException ce){
             JOptionPane.showMessageDialog(rootPane,ce.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);

        }
        
      }  
      
    }//GEN-LAST:event_BotaoFornecedorClienteActionPerformed

