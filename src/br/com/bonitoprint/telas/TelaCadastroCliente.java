    private void BotaoSalvarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoSalvarClienteActionPerformed
        // TODO add your handling code here:
        String nome = CampoClienteNome.getText();
        String email = CampoClienteEmail.getText();
        String cpf = CampoClienteCpf.getText();
        String rua = CampoClienteRua.getText();
        String complemento = CampoClienteComplemento.getText();
        String bairro = CampoClienteBairro.getText();
        String cidade = CampoClienteCidade.getText();
        String estado = CampoEstado.getText();
        String tel1= CampoTel1.getText();
        String tel2 = CampoTel2.getText();
        String num = CampoNumero.getText();
        if(num.length()<=0){
           JOptionPane.showMessageDialog(rootPane,"Campo Obrigatorio", "ERRO", JOptionPane.ERROR_MESSAGE);
        }else{
        int numero = Integer.parseInt(CampoNumero.getText());
        Cliente cliente = new Cliente(tel1,tel2,nome,email,cpf,rua,numero,complemento,bairro,cidade,estado);
       
        ClienteController c = new ClienteController();
        try{
            c.adicionar(cliente);
            JOptionPane.showMessageDialog(rootPane,"Cliente Cadastrado com Sucesso", "Cadastro de Usuario", JOptionPane.INFORMATION_MESSAGE);
            CampoClienteNome.setText("");
            CampoClienteEmail.setText("");
            CampoClienteCpf.setText("");
            CampoClienteRua.setText("");
            CampoNumero.setText("");
            CampoClienteComplemento.setText("");
            CampoClienteBairro.setText("");
            CampoClienteCidade.setText("");
            CampoEstado.setText("");
            CampoTel1.setText("");
            CampoTel2.setText("");
        }catch(UsuarioExistenteException e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }    
        catch(ErroInternoException erro){
             JOptionPane.showMessageDialog(rootPane, erro.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }catch(CampoObrigatorioException ex){
            JOptionPane.showMessageDialog(rootPane, ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        } 
    }//GEN-LAST:event_BotaoSalvarClienteActionPerformed

