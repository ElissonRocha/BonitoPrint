    private void BotaoPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoPesquisarActionPerformed
        // TODO add your handling code here:
         String pesquisar = this.CampoPesqNome.getText();
         ProdutoController pcontroller = new ProdutoController();
         List<Produto> lista = pcontroller.listar(pesquisar);
         ComboProduto.removeAllItems();
      
         for(int i=0;i<lista.size();i++){
             System.out.println("Entrou no for");
             System.out.println(lista.get(i));
             ComboProduto.addItem(lista.get(i).getNome());
         }
    }//GEN-LAST:event_BotaoPesquisarActionPerformed

    private void BotaoSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoSelecionarActionPerformed
        // TODO add your handling code here:
        String select = ComboProduto.getSelectedItem().toString();
        ProdutoController pcontroller = new ProdutoController();
        Produto prod = new Produto();
        try {
            prod = pcontroller.consultar(select);
            CampoNome.setText(""+prod.getNome());
            CampoQuantidade.setText(""+prod.getQuantidade());
            CampoPreco.setText(""+prod.getPreco());
         
        } catch (UsuarioInexistenteException ex) {
           JOptionPane.showMessageDialog(rootPane, ex);
        }
        
    }//GEN-LAST:event_BotaoSelecionarActionPerformed

    private void AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AlterarActionPerformed
        // TODO add your handling code here:
        String nome = CampoNome.getText();
        int quant = Integer.parseInt(CampoQuantidade.getText());
        double preco = Double.parseDouble(CampoPreco.getText());
        
    
        Produto produto = new Produto(nome,quant,preco);
        ProdutoController pcontroller = new ProdutoController();
        
        try{
            pcontroller.atualizar(produto);
        }catch(ErroInternoException e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_AlterarActionPerformed

    private void AddQuantidadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddQuantidadeActionPerformed
        // TODO add your handling code here:
        String nome = CampoNome.getText();
        double preco = Double.parseDouble(CampoPreco.getText());
        int quant = Integer.parseInt(CampoQuantidade.getText());
        int addquant = Integer.parseInt(CampoAddQuantidade.getText());
        int total = quant + addquant;
        this.CampoQuantidade.setText(""+total);
        
        Produto produto = new Produto(nome,total,preco);
        ProdutoController pcontroller = new ProdutoController();
        
        try{
            pcontroller.atualizar(produto);
        }catch(ErroInternoException e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_AddQuantidadeActionPerformed

    private void DeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeletarActionPerformed
        // TODO add your handling code here:
         String nome = CampoNome.getText();
         ProdutoController pcontroller = new ProdutoController();
        try {
            JOptionPane.showMessageDialog(rootPane, JOptionPane.CANCEL_OPTION);
            pcontroller.deletar(nome);
        }catch (ErroInternoException ex) {
           JOptionPane.showMessageDialog(rootPane, ex);
        }catch(UsuarioInexistenteException e){
            
        }
        
    }//GEN-LAST:event_DeletarActionPerformed

